package sprites;

/**
 * A class that represents the Dumpster Sprite.
 */
public class Dumpster extends Sprite {

	/**
	 * Initializes a Dumpster with the given symbol at the given 
	 * row and column.
	 * 
	 * @param symbol	char representing a Dumpster
	 * @param row		the row in which this Dumpster is located
	 * @param column	the column in which this Dumpster is located
	 */
	public Dumpster(char symbol, int row, int column) {
		super(symbol, row, column);
	}
}
